"""
Обнулить (выключить) конкретный бит.

Идея: маска с нулём на нужной позиции и единицами везде остальное.
AND с такой маской «проталкивает» ноль на эту позицию, не трогая остальные биты
(если в маске 1, бит из числа копируется как есть).
"""

# 0x16 = 0001 0110
a = 0x16
# 0xFB = 1111 1011 — на позиции бита 2 стоит 0, остальные биты маски = 1.
a = a & 0xFB
print(a)

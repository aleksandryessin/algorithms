"""
Установить бит в 1 (включить бит).

Операция OR (|): если в маске на какой-то позиции 1, то в результате там тоже будет 1,
независимо от исходного значения бита в числе (0|1=1, 1|1=1).
"""

# 0x16 = 0001 0110
a = 0x16
# 0x20 = 0010 0000 — включаем бит на позиции 5.
a = a | 0x20
# Ожидаемо: 0x36 = 0011 0110
print(a)

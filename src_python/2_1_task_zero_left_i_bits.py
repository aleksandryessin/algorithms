"""
Обнулить старшие i бит в рамках рассматриваемого фрагмента (здесь — оставить только младшие 3 бита).

Маска из единиц только «справа»: (2^k - 1) даёт k единиц подряд с позиции 0.
"""

# 0x6B = 0110 1011
a = 0x6B
# 7 = 0b111 — оставляем только три младших бита, остальное обнуляем.
a = a & 7
print(a)

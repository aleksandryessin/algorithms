"""
Обнулить младшие i бит числа (справа в двоичной записи).

Маска из единиц слева и нулей справа: при AND младшие позиции принудительно становятся 0.
Здесь i = 4: младшие 4 бита обнуляются.
"""

# 0x6B = 0110 1011
a = 0x6B
# 0xF0 = 1111 0000 — сохраняем верхние 4 бита, нижние 4 обнуляем.
a = a & 0xF0
print(a)

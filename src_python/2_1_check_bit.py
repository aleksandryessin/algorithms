"""
Раздел 2: битовые операции.

Задача: проверить, установлен ли конкретный бит в числе.

Идея: «маска» — число, в котором ровно один бит равен 1 на нужной позиции.
Операция AND (&) оставляет только те биты, где в ОБОИХ числах единицы.
Если результат не ноль — интересующий бит в `a` был 1.
"""

# 0x02 = 0000 0010 в двоичном виде — единица на позиции бита 1 (считая от 0 справа).
def is_bit(a: int) -> bool:
    # Сохраняем только тот бит из `a`, который совпадает с маской; остальные станут 0.
    result = a & 0x02
    # Любой ненулевой результат означает: этот бит в исходном числе был установлен.
    return result != 0


if __name__ == "__main__":
    # 0x16 = 22 = 0001 0110 — на позиции бита 1 стоит 1, поэтому is_bit вернёт True.
    a = 0x16
    print(is_bit(a))

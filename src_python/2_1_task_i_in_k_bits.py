"""
Разместить значение бита в нужной позиции (упрощённый пример из задачи про i и k).

Здесь: число a = 0x63, нужно гарантировать установленный бит в определённой позиции
и при этом ограничить результат одним байтом (младшие 8 бит).
"""

# 0x63 = 0110 0011
a = 0x63
# | 8 — включаем бит, соответствующий константе 8 (0000 1000).
# & 0xFF — обрезаем до 8 бит (в Python int не переполняется, но для «байта» так делают явно).
result = (a | 8) & 0xFF
print(result)

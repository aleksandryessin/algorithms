"""
2.2 — то же по смыслу, что и обнуление младших i бит: маска отсекает «хвост» справа.

В этом файле демонстрируется AND с маской 0xF0: верхняя половина битов (в рамках байта)
сохраняется, нижняя обнуляется. Это частый приём при выравнивании по границе битов.
"""

# 0x6B = 0110 1011
a = 0x6B
# 0xF0 = 1111 0000 — «отрезаем» младшие 4 бита.
a = a & 0xF0
print(a)

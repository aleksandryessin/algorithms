"""
Удаление дубликатов из отсортированного массива «на месте» (два указателя).

Инвариант: подмассив arr[0:write) содержит только уникальные значения в том же порядке,
что и в исходном массиве при первом вхождении.

Указатель read скользит по всем элементам; write — куда писать следующий «новый» уникальный.
Сравниваем arr[read] с последним записанным arr[write-1]: если совпало — дубликат, пропускаем;
если нет — копируем arr[read] в arr[write] и сдвигаем write.

Сложность: O(N) по времени, O(1) дополнительной памяти (не считая самого массива).
"""

def remove_duplicates(arr: list) -> int:
    if len(arr) <= 1:
        return len(arr)

    write = 1
    for read in range(1, len(arr)):
        # Последний уникальный — на позиции write-1; если текущий такой же — это повтор.
        if arr[read] != arr[write - 1]:
            arr[write] = arr[read]
            write += 1

    return write


if __name__ == "__main__":
    arr = [1, 2, 2, 3, 3, 3, 4, 7, 7, 7, 7]
    new_len = remove_duplicates(arr)
    print(new_len)  # 5 — длина префикса без дубликатов
    print(arr[:new_len])  # [1, 2, 3, 4, 7]

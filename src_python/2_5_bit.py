"""
Вставить младшие биты числа B в «окно» битов числа A между позициями end и start (включительно снизу).

Общая схема:
1) В A вырезать этот диапазон (сделать там нули) — две маски: «всё выше start» и «всё ниже end»,
   объединяем через OR и инвертируем смысл: оставляем единицы везде, кроме окна.
2) Подготовить B: сдвинуть влево на `end`, чтобы биты встали в нужное место;
   обрезать лишнее сверху маской (1<<(start+1))-1.
3) Склеить: A & hole_mask | подготовленный B; при необходимости обрезать до 32 бит.

В Python int бесконечной длины, поэтому маски явно ограничивают 32 битами там, где нужно
имитировать поведение фиксированного слова (как в C/Java).
"""

A = 285689642
B = 1132478069
start = 16
end = 5

# Старшие биты: единицы от 31-го (в типичной 32-битной картинке) вниз до (start+1).
# -1 << k в дополнении до двоичного даёт «все единицы с позиции k вверх».
mask_l = (-1 << (start + 1)) & 0xFFFFFFFF

# Младшие end бит: формула (1 << end) - 1 даёт подряд end единиц с позиции 0.
mask_r = (1 << end) - 1

# Объединение: единицы «слева от окна» и «справа от окна» — между ними в итоговой маске нули
# в позициях, которые мы хотим очистить в A.
mask_a = mask_l | mask_r
A &= mask_a

# Сдвигаем биты B так, чтобы «младшая часть» встала начиная с позиции end.
B <<= end

# Оставляем в B только те биты, которые помещаются в окно до start включительно.
mask_b = (1 << (start + 1)) - 1
B &= mask_b

result = (A | B) & 0xFFFFFFFF
# В беззнаковом 32-битном мире значения >= 2^31 трактуются как отрицательные в int32.
if result >= 0x80000000:
    result -= 0x100000000
print(result)
